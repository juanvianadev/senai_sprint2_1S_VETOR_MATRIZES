def mult_mat(A, B):
    if len(A[0]) != len(B):
        raise ValueError("Dimensoes incompativeis")

    nl = len(A)  # linhas do resultado
    nc = len(B[0])  # colunas do resultado

    resultado = [[ 0 for _ in range(nc)]
                     for _ in range(nl)]

    for i in range(nl):
        for j in range(nc):
            for k in range(len(B)):
                resultado[i][j] += A[i][k]*B[k][j]

    return resultado


import numpy as np

def transp(mat):
    """
    mat = [[2, 3], [4, 5], [6, 7]]

    [[2, 3],
     [4, 5]
     [6, 7]]

     mat_transposta:
     [[2, 4, 6],
      [3, 5, 7]]
    """
    nl = len(mat)  # linhas na matriz mat
    nc = len(mat[0])  # colunas em mat
    tr = []
    for j in range(nc):
        linha = []
        for i in range(nl):
            linha.append(mat[i][j])
        tr.append(linha)
    return tr

mat = [[2, 3], [4, 5], [6, 7]]
